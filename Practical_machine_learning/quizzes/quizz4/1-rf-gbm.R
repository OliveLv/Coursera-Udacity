library(ElemStatLearn)
library(caret)
data(vowel.train)
data(vowel.test) 
vowel.train$y<-as.factor(vowel.train$y)
vowel.test$y<-as.factor(vowel.test$y)
set.seed(33833)
mod1<-train(y~.,method="rf",data=vowel.train)
mod2<-train(y~.,method="gbm",data=vowel.train)
pred1<-predict(mod1,vowel.test)
pred2<-predict(mod2,vowel.test)
confusion1<-confusionMatrix(pred1,vowel.test$y)
confusion2<-confusionMatrix(pred2,vowel.test$y)
confusion3<-confusionMatrix(pred1,pred2)